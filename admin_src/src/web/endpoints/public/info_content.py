def _brand(text: str) -> str:
    return text.replace("{brand}", resolve_brand_name())


def _branded_defaults() -> dict[str, Any]:
    return {
        "faq": [{"q": _brand(i["q"]), "a": _brand(i["a"])} for i in DEFAULT_FAQ],
        **{k: _brand(v) for k, v in DEFAULT_TEXT.items()},
    }


def load_stored() -> dict[str, Any]:
    """Сырой сохранённый контент (то, что записал админ). {} — если файла нет."""
    try:
        if INFO_PATH.exists():
            with INFO_PATH.open(encoding="utf-8") as fh:
                data = json.load(fh)
            if isinstance(data, dict):
                return data
    except Exception:
        pass
    return {}


def effective_content() -> dict[str, Any]:
    """Что реально показывать: сохранённое поверх брендированных дефолтов."""
    data = _branded_defaults()
    stored = load_stored()
    if isinstance(stored.get("faq"), list):
        data["faq"] = [
            {"q": str(i.get("q", "")), "a": str(i.get("a", ""))}
            for i in stored["faq"]
            if isinstance(i, dict)
        ]
    for key in TEXT_SECTIONS:
        if isinstance(stored.get(key), str):
            data[key] = stored[key]
    return data


@router.get("")
async def get_info() -> dict[str, Any]:
    """Публично: контент страницы «Информация» (сохранённый или дефолтный)."""
    return effective_content()
